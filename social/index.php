										<h2>Social Cause</h2>
									</header>
									<p>St Xavier’s College has always upheld the lofty standards of higher education deemed necessary for the youth. However at the same time, we have also believed in the aim of “Education for all” in keeping with the Jesuit traditions of our college by taking it to the grassroots and inculcating a change at that level-to bear fruit at the highest of peaks.</p>
									</p>The Malhar Organising Committee has decided to ensure that this worthy cause is integrated into the college festival this year and we present to you our social cause- &quot;Seekho-Sikhao &quot;.</p>
									</p>This venture enables college students to put forward their proposals for sustainable education models and their implementation. NGOs will bring forth their expertise to mentor and guide the students in their selected avenue of Education. </p>
									</p>4 sectors of education can be chosen for their model, these include-Food and Nutrition, Infrastructure, Adult Literacy and Primary education.</p>
									</p>The Social Cause serves to take note of Malhar’s valiant efforts to give back to society, a little of what we have received from them. Noteworthy previous causes have included Chaka Chak, the beach clean-up drive as well as the Malhar Yard Sale which donates all proceeds to charity.</p>
									<hr>
									<br/><br/>
									<h3>Sample Format for Social Cause Model:</h3>
									<ol>
									<li>Statement of Proposal: E.g.:Sector: Food and Nutrition. Proposal: Improving mid-day meal schemes for the under-privileged students in rural schools.(Include a reason for choosing this particular sector)</li>
									<li>Brief Synopsis of the aims along with the dynamics of the project. E.g.: Materials required for improving the mid-day meal schemes in schools, cost-effective ways to procure the required materials, other ways to improve nutrition overall in rural children. </li>
									<li>Feasibility of the project: Cost effectiveness, duration of the project.</li>
