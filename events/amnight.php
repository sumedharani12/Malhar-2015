          <h4><u>Please Note:</u></h4>
          
          <ol style="text-align:left;">
            <li>No CAs are required.</li>
            <li>College IDs of participants are required at the time of registration.</li>
            <li>The songs may be in Hindi, English or any other foreign language. Also, Indian semiclassical and classical songs are allowed.</li>
            <li>Original compositions are permitted.</li>
            <li>The use of audio cds/instruments is permitted in the auditions as well as in the final round.</li>
            <li>The use of body percussion will be appreciated.
</li>
            <li>All acoustic instruments are allowed.</li>
            <li>Vulgarity and obscenity with respect to song lyrics/ performance and costume are strictly prohibited. In case of vulgarity/obscenity the participants will be disqualified at the Organising Committee's discretion.</li>
            <li>A list of lyrics of the songs being performed along with the instruments being used and technical requirements by those participants that are chosen for the finals must be submitted at a date given prior, by the Malhar Organising Committee.</li>
            <li>Substitution of participants from the auditions round to the final round will not be encouraged. In the event of its occurrence, the course of action to be followed will be decided upon by the Organising Committee.</li>
            <li>If participants belonging to the solo/duet/Beatboxing category wish to use A1 tracks, they must be in Audio Cd format only. Participants are urged to carry a backup CD.</li>
            <li>The A1 tracks used must have no harmonies, voiceovers or pre recorded percussions.</li>
            <li>Distortion will not be permitted. Failure to adhere to this rule will lead to immediate disqualification.</li>
            <li>Pre programmed patches and voice processors will not be permitted.</li>
